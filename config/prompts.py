# ==================== Prompt管理函数 ====================

SYSTEM_PROMPTS: Dict[str, str] = {
    "orchestrator": ORCHESTRATOR_PROMPT,
    "fundamental_analyst": FUNDAMENTAL_ANALYST_PROMPT,
    "technical_analyst": TECHNICAL_ANALYST_PROMPT,
    "sentiment_analyst": SENTIMENT_ANALYST_PROMPT,
    "news_analyst": NEWS_ANALYST_PROMPT,
    "capital_flow_analyst": CAPITAL_FLOW_ANALYST_PROMPT,
    "report_retriever": REPORT_RETRIEVER_PROMPT,
    "debate": DEBATE_PROMPT,
    "portfolio_manager": PORTFOLIO_MANAGER_PROMPT,
    "risk_controller": RISK_CONTROLLER_PROMPT,
    "compliance_officer": COMPLIANCE_OFFICER_PROMPT,
    "qa_assistant": QA_ASSISTANT_PROMPT,
    "report_generator": REPORT_GENERATION_PROMPT,
}


def get_prompt(agent_name: str, context: Optional[Dict[str, Any]] = None) -> str:
    """
    获取Agent的System Prompt
    
    Args:
        agent_name: Agent名称
        context: 上下文变量
        
    Returns:
        格式化后的Prompt
    """
    base_prompt = SYSTEM_PROMPTS.get(agent_name, "")
    
    if context and "{" in base_prompt:
        try:
            return base_prompt.format(**context)
        except KeyError:
            pass
    
    return base_prompt


def get_intent_classification_prompt() -> str:
    """获取意图分类Prompt"""
    return """分析用户输入，判断意图类型：

意图类型：
- stock_analysis: 股票深度分析（包含股票代码/名称 + 分析意图）
- quick_qa: 快速问答（简短问题，需要快速回答）
- report_search: 研报检索（明确要求查找研报或报告）
- backtest: 策略回测（要求回测某策略）
- unknown: 无法识别

请只输出意图类型，不要输出其他内容。"""


